<template>
  <section v-loading="listLoading" class="page-container">
    <div class="toolbar">
      <el-form :inline="true" :model="filters">
        <el-form-item>
          <el-input v-model="filters.id" placeholder="编号"></el-input>
        </el-form-item>
        <el-form-item>
          <el-input v-model="filters.userId" placeholder="用户编号"></el-input>
        </el-form-item>
        <el-form-item>
          <el-input v-model="filters.title" placeholder="标题"></el-input>
        </el-form-item>
        <el-form-item>
          <el-select
            v-model="filters.recommend"
            @change="list"
            clearable
            placeholder="是否推荐"
          >
            <el-option label="推荐" value="1"></el-option>
            <el-option label="未推荐" value="0"></el-option>
          </el-select>
        </el-form-item>
        <el-form-item>
          <el-select
            v-model="filters.status"
            @change="list"
            clearable
            placeholder="请选择状态"
          >
            <el-option label="正常" value="0"></el-option>
            <el-option label="删除" value="1"></el-option>
          </el-select>
        </el-form-item>
        <el-form-item>
          <el-button v-on:click="list" type="primary">查询</el-button>
        </el-form-item>
      </el-form>
    </div>
    <div class="pagebar">
      <el-pagination
        :page-sizes="[20, 50, 100, 300]"
        @current-change="handlePageChange"
        @size-change="handleLimitChange"
        :current-page="page.page"
        :page-size="page.limit"
        :total="page.total"
        layout="total, sizes, prev, pager, next, jumper"
      >
      </el-pagination>
    </div>
    <div class="topics main-content">
      <div v-for="item in results" :key="item.id" class="topic">
        <div class="topic-header">
          <img :src="item.user.avatar" class="avatar" />
          <div class="topic-right">
            <div class="topic-title">
              <a :href="'/topic/' + item.id" target="_blank">{{
                item.title
              }}</a>
            </div>
            <div class="topic-meta">
              <label v-if="item.user" class="author">{{
                item.user.nickname
              }}</label>
              <label>{{ item.createTime | formatDate }}</label>
              <label class="node">{{ item.node ? item.node.name : '' }}</label>
              <label v-for="tag in item.tags" :key="tag.tagId" class="tag">{{
                tag.tagName
              }}</label>
            </div>
          </div>
        </div>

        <div class="summary">
          {{ item.summary }}
        </div>

        <div class="topic-footer">
          <span v-if="item.status === 1" class="danger">已删除</span>
          <span class="info">编号：{{ item.id }}</span>

          <a v-if="item.status === 0" @click="deleteSubmit(item)" class="btn"
            >删除</a
          >
          <a v-else @click="undeleteSubmit(item)" class="btn">取消删除</a>

          <a v-if="!item.recommend" @click="recommend(item.id)" class="btn"
            >推荐</a
          >
          <a v-else @click="cancelRecommend(item.id)" class="btn">取消推荐</a>
        </div>
      </div>
    </div>

    <div class="pagebar">
      <el-pagination
        :page-sizes="[20, 50, 100, 300]"
        @current-change="handlePageChange"
        @size-change="handleLimitChange"
        :current-page="page.page"
        :page-size="page.limit"
        :total="page.total"
        layout="total, sizes, prev, pager, next, jumper"
      >
      </el-pagination>
    </div>

    <el-dialog
      :visible.sync="addFormVisible"
      :close-on-click-modal="false"
      title="新增"
    >
      <el-form
        ref="addForm"
        :model="addForm"
        :rules="addFormRules"
        label-width="80px"
      >
        <el-form-item label="userId" prop="rule">
          <el-input v-model="addForm.userId"></el-input>
        </el-form-item>

        <el-form-item label="title" prop="rule">
          <el-input v-model="addForm.title"></el-input>
        </el-form-item>

        <el-form-item label="content" prop="rule">
          <el-input v-model="addForm.content"></el-input>
        </el-form-item>

        <el-form-item label="status" prop="rule">
          <el-input v-model="addForm.status"></el-input>
        </el-form-item>

        <el-form-item label="createTime" prop="rule">
          <el-input v-model="addForm.createTime"></el-input>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button @click.native="addFormVisible = false">取消</el-button>
        <el-button
          @click.native="addSubmit"
          :loading="addLoading"
          type="primary"
          >提交</el-button
        >
      </div>
    </el-dialog>

    <el-dialog
      :visible.sync="editFormVisible"
      :close-on-click-modal="false"
      title="编辑"
    >
      <el-form
        ref="editForm"
        :model="editForm"
        :rules="editFormRules"
        label-width="80px"
      >
        <el-input v-model="editForm.id" type="hidden"></el-input>

        <el-form-item label="forumId" prop="rule">
          <el-input v-model="editForm.forumId"></el-input>
        </el-form-item>

        <el-form-item label="userId" prop="rule">
          <el-input v-model="editForm.userId"></el-input>
        </el-form-item>

        <el-form-item label="title" prop="rule">
          <el-input v-model="editForm.title"></el-input>
        </el-form-item>

        <el-form-item label="content" prop="rule">
          <el-input v-model="editForm.content"></el-input>
        </el-form-item>

        <el-form-item label="status" prop="rule">
          <el-input v-model="editForm.status"></el-input>
        </el-form-item>

        <el-form-item label="createTime" prop="rule">
          <el-input v-model="editForm.createTime"></el-input>
        </el-form-item>
      </el-form>
      <div slot="footer" class="dialog-footer">
        <el-button @click.native="editFormVisible = false">取消</el-button>
        <el-button
          @click.native="editSubmit"
          :loading="editLoading"
          type="primary"
          >提交</el-button
        >
      </div>
    </el-dialog>
  </section>
</template>

<script>
export default {
  layout: 'admin',
  data() {
    return {
      results: [],
      listLoading: false,
      page: {},
      filters: {
        status: '0'
      },
      selectedRows: [],
      addForm: {
        forumId: '',
        userId: '',
        title: '',
        content: '',
        status: '',
        createTime: ''
      },
      addFormVisible: false,
      addFormRules: {},
      addLoading: false,

      editForm: {
        id: '',
        forumId: '',
        userId: '',
        title: '',
        content: '',
        status: '',
        createTime: ''
      },
      editFormVisible: false,
      editFormRules: {},
      editLoading: false
    }
  },
  mounted() {
    this.list()
  },
  methods: {
    list() {
      const me = this
      me.listLoading = true
      const params = {}
      params.params = Object.assign(me.filters, {
        page: me.page.page,
        limit: me.page.limit
      })

      this.$axios
        .get('/api/admin/topics', params)
        .then((data) => {
          me.results = data.results
          me.page = data.page
        })
        .finally(() => {
          me.listLoading = false
        })
    },
    handlePageChange(val) {
      this.page.page = val
      this.list()
    },
    handleLimitChange(val) {
      this.page.limit = val
      this.list()
    },
    handleAdd() {
      this.addForm = {
        name: '',
        description: ''
      }
      this.addFormVisible = true
    },
    addSubmit() {
      const me = this
      this.$axios
        .post('/api/admin/topics', this.addForm)
        .then((data) => {
          me.$message({ message: '提交成功', type: 'success' })
          me.addFormVisible = false
          me.list()
        })
        .catch((rsp) => {
          me.$notify.error({ title: '错误', message: rsp.message })
        })
    },
    handleEdit(index, row) {
      const me = this
      this.$axios
        .get(`/api/admin/topic/${row.id}`)
        .then((data) => {
          me.editForm = Object.assign({}, data)
          me.editFormVisible = true
        })
        .catch((rsp) => {
          me.$notify.error({ title: '错误', message: rsp.message })
        })
    },
    editSubmit() {
      const me = this
      this.$axios
        .put('/api/admin/topics/' + me.editForm.id, me.editForm)
        .then((data) => {
          me.list()
          me.editFormVisible = false
        })
        .catch((rsp) => {
          me.$notify.error({ title: '错误', message: rsp.message })
        })
    },
    async deleteSubmit(row) {
      await this.$confirm('是否确认删除该话题?', '提示', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      })
        .then((confirm) => {
          try {
            this.$axios.delete('/api/admin/topics/' + row.id)
            this.$message({ message: '删除成功', type: 'success' })
            this.list()
          } catch (err) {
            this.$notify.error({ title: '错误', message: err.message || err })
          }
        })
        .catch((cancel) => {
          console.log('cancel')
        })
    },
    async undeleteSubmit(row) {
      try {
        await this.$axios.post('/api/admin/topics/' + row.id + '/undelete')
        this.list()
        this.$message({ message: '取消删除成功', type: 'success' })
      } catch (err) {
        this.$notify.error({ title: '错误', message: err.message || err })
      }
    },
    async recommend(id) {
      try {
        await this.$axios.post('/api/admin/topics/' + id + '/recommend')
        this.$message({ message: '推荐成功', type: 'success' })
        this.list()
      } catch (e) {
        this.$notify.error({ title: '错误', message: e.message })
      }
    },
    async cancelRecommend(id) {
      try {
        await this.$axios.post('/api/admin/topics/' + id + '/unrecommend')
        this.$message({ message: '取消推荐成功', type: 'success' })
        this.list()
      } catch (e) {
        this.$notify.error({ title: '错误', message: e.message })
      }
    },
    handleSelectionChange(val) {
      this.selectedRows = val
    }
  }
}
</script>

